LIMIT_FILE = "limits.json"

def load_limits() -> dict:
    if os.path.exists(LIMIT_FILE):
        with open(LIMIT_FILE, "r") as f:
            return json.load(f)
    return {}

def save_limits(data: dict):
    with open(LIMIT_FILE, "w") as f:
        json.dump(data, f)

def can_draw(user_id: int) -> bool:
    limits = load_limits()
    return limits.get(str(user_id)) != str(date.today())

def mark_draw(user_id: int):
    limits = load_limits()
    limits[str(user_id)] = str(date.today())
    save_limits(limits)

