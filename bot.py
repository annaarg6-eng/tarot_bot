def format_card(card: dict, reversed: bool, show_advice: bool = True) -> str:
    suit_emoji = {"wands": "🔥", "cups": "💧", "swords": "⚔️", "pentacles": "🪙"}
    arcana_label = "Старший аркан" if card["arcana"] == "major" else f"Младший аркан • {suit_emoji.get(card.get('suit',''), '')} {card.get('suit','').capitalize()}"

    position = "🔄 Перевёрнутая" if reversed else "⬆️ Прямая"
    meaning = card["meaning_reversed"] if reversed else card["meaning_upright"]
    advice = card["advice_reversed"] if reversed else card["advice_upright"]

    text = (
        f"🃏 *{card['name']}*\n"
        f"_{arcana_label}_ • {position}\n\n"
        f"*Значение:* {meaning}\n\n"
    )
    if show_advice:
        text += f"*Совет карты:*\n{advice}"
    return text

